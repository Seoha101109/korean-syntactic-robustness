def cs(data1,data2):
  result = []
  for i in range(len(data1)):
    v1 = data1[i]
    v2 = data2[i]

    dot_product = np.dot(v1, v2)
    norm_v1 = np.linalg.norm(v1)
    norm_v2 = np.linalg.norm(v2)

    sim = dot_product / (norm_v1 * norm_v2 + 1e-8)
    result.append(sim)
  return result

