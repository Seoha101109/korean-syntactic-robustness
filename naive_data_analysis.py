def linear_CKA(X, Y):
    X = X - X.mean(axis=0, keepdims=True)
    Y = Y - Y.mean(axis=0, keepdims=True)

    K = X @ X.T
    L = Y @ Y.T

    def hsic(A, B):
        N = A.shape[0]
        H = np.eye(N) - (1.0 / N)
        return np.sum((H @ A @ H) * (H @ B @ H))

    hsic_kl = hsic(K, L)
    hsic_kk = hsic(K, K)
    hsic_ll = hsic(L, L)

    if hsic_kk * hsic_ll == 0:
        return 0
    return hsic_kl / np.sqrt(hsic_kk * hsic_ll)

